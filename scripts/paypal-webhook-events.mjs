export function toPaypalEventType(label) {
  return String(label || "")
    .trim()
    .replace(/\s+/g, ".")
    .toUpperCase();
}

export function buildWebhookEventCatalog(labels = []) {
  return labels.map((label) => ({
    label,
    eventType: toPaypalEventType(label)
  }));
}

export const sandboxWebhookEvents = buildWebhookEventCatalog(sandboxWebhookEventLabels);
export const liveWebhookEvents = buildWebhookEventCatalog(liveWebhookEventLabels);
